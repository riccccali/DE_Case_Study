/* CREATE RAW TABLES INGESTION SNOWPIPES */
CREATE OR REPLACE PIPE REDBULL_DATA.BRONZE.MATCHING_PIPE
AUTO_INGEST = TRUE
AS
COPY INTO REDBULL_DATA.BRONZE.MATCHING_RAW (MARKET, SOURCE_FILE, FILE_ROW_NUMBER, LOAD_TIMESTAMP, RAW_DATA)
FROM (
  SELECT 
    SPLIT_PART(METADATA$FILENAME, '/', 2), 
    METADATA$FILENAME,                     
    METADATA$FILE_ROW_NUMBER,              
    CURRENT_TIMESTAMP(),
    TO_VARCHAR($1)
  FROM @REDBULL_DATA.BRONZE.RAW_STAGE/MATCHING/
)
FILE_FORMAT = (FORMAT_NAME = 'RAW_FORMAT')
ON_ERROR = SKIP_FILE;

