/* CREATE RAW FILE FORMAT */
CREATE OR REPLACE FILE FORMAT REDBULL_DATA.BRONZE.RAW_FORMAT
    TYPE = 'CSV'
    FIELD_DELIMITER = NONE
    RECORD_DELIMITER = '\n'
    SKIP_HEADER = 1
    TRIM_SPACE = TRUE
    SKIP_BYTE_ORDER_MARK = TRUE
    ENCODING = 'UTF8'
    NULL_IF = ('NULL', 'null', '');

/* CREATE QUOTE-AWARE TSV FILE FORMAT FOR PARSED BRONZE TABLES */
CREATE OR REPLACE FILE FORMAT REDBULL_DATA.BRONZE.TSV_FORMAT
    TYPE = 'CSV'
    FIELD_DELIMITER = '\t'
    RECORD_DELIMITER = '\n'
    FIELD_OPTIONALLY_ENCLOSED_BY = '"'
    SKIP_HEADER = 1
    TRIM_SPACE = TRUE
    SKIP_BYTE_ORDER_MARK = TRUE
    ENCODING = 'UTF8'
    EMPTY_FIELD_AS_NULL = TRUE
    NULL_IF = ('NULL', 'null', '');

